import mongoose from "mongoose";
const UsersSchema = new mongoose.Schema(
  {
    fullname: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    phone: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    isAdmin: {
      type: Boolean,
      default: "",
    },
  },
  { timestamps: true }
);

export default mongoose.model("users", UsersSchema);
